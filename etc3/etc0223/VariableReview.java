package etc3.etc0223;

public class VariableReview {
	public static void main(String[] args) {
		byte num = 100;
		short num1 = 1000;
		int num2 = 10000;
		long num3 = 1000000000;

		System.out.printf("현재 출력되는 값은 정수형이며 값은 %d입니다.\n", num2);
		// 정수 자료형 중 가장 많이 사용되는 자료형은 int입니다.
		double d = 1.23456789;
		System.out.printf("현재 출력되는 값은 실수형이며 값은 %10.8f입니다.\n", d);
		//실수 자료형 중 가장 많이 사용되는 자료형은 double입니다.
		
		char c = 'a';
		System.out.printf("현재 출력되는 문자는 %c입니다.\n", c);
		
		boolean flag = false;
		
	}
}
